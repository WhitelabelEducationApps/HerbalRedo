def main():
    conn = sqlite3.connect(DB_PATH)
    cur = conn.cursor()

    cur.execute("""
        SELECT id,
               CASE WHEN unicode(paintingname_ja)=63 THEN 1 ELSE 0 END,
               CASE WHEN unicode(paintingname_zh)=63 THEN 1 ELSE 0 END,
               CASE WHEN unicode(paintingname_ru)=63 THEN 1 ELSE 0 END,
               CASE WHEN unicode(paintingname_ar)=63 THEN 1 ELSE 0 END,
               CASE WHEN unicode(paintingname_hi)=63 THEN 1 ELSE 0 END
        FROM museum_item
        WHERE unicode(paintingname_ja)=63 OR unicode(paintingname_zh)=63
           OR unicode(paintingname_ru)=63 OR unicode(paintingname_ar)=63
           OR unicode(paintingname_hi)=63
    """)
    rows = cur.fetchall()

    updated = 0
    missing = []
    for pid, ja_bad, zh_bad, ru_bad, ar_bad, hi_bad in rows:
        if pid not in TRANSLATIONS:
            missing.append(pid)
            continue
        t = TRANSLATIONS[pid]
        sets, vals = [], []
        for lang, bad in [('ja',ja_bad),('zh',zh_bad),('ru',ru_bad),('ar',ar_bad),('hi',hi_bad)]:
            if bad and lang in t:
                sets.append(f'paintingname_{lang}=?')
                vals.append(t[lang])
        if sets:
            vals.append(pid)
            cur.execute(f"UPDATE museum_item SET {','.join(sets)} WHERE id=?", vals)
            updated += 1

    conn.commit()
    conn.close()
    print(f"Updated: {updated}")
    if missing:
        print(f"Missing in dict: {missing}")

    conn2 = sqlite3.connect(DB_PATH)
    cur2 = conn2.cursor()
    for lang in ['ja','zh','ru','ar','hi']:
        cur2.execute(f"SELECT COUNT(*) FROM museum_item WHERE unicode(paintingname_{lang})=63")
        print(f"  {lang} remaining corrupted: {cur2.fetchone()[0]}")
    conn2.close()

if __name__ == '__main__':
    main()
